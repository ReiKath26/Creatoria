    static let rosyPink: UIColor = UIColor(named: "Rosy Pink") ?? UIColor.red
    static let sunnyYellow: UIColor = UIColor(named: "Sun Yellow") ?? UIColor.yellow
    static let lightRed : UIColor = UIColor(named: "Light red") ?? UIColor.red
}
